a = [1, -20, 38, 0, 44]
b = [88, -20, 48, 4, 33, 2]
i = 0

x = min(len(a), len(b))

print('Сравним попарно элементы массивов:\n')

while i < x:
    s = 'Сравним {} и {}'
    print(s.format(a[i], b[i]))
    print('Наименьшее число - ' + str(min(a[i],b[i])))
    if (abs(a[i] - b[i]) < 15):
        s = 'Поздравляем! Разница между элементами {} и {} меньше 15ти.'
        print(s.format(a[i], b[i]))
    print ('\n')
    i = i+1
